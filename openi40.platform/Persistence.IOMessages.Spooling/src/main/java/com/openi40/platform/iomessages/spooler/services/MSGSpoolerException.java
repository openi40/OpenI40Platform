package com.openi40.platform.iomessages.spooler.services;

public class MSGSpoolerException extends Exception {

	public MSGSpoolerException() {
		
	}

	public MSGSpoolerException(String message) {
		super(message);
		
	}

	public MSGSpoolerException(Throwable cause) {
		super(cause);
		
	}

	public MSGSpoolerException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public MSGSpoolerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
