public class OutputDataStreamException extends Exception {

	public OutputDataStreamException() {
		
	}

	public OutputDataStreamException(String message) {
		super(message);
		
	}

	public OutputDataStreamException(Throwable cause) {
		super(cause);
		
	}

	public OutputDataStreamException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public OutputDataStreamException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}
	

}
